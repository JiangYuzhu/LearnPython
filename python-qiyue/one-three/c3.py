
#进制，各进制的表示和转换
#各进制的表示
print(0b10)
print(0o11)
print(0x10)
print(11)
#bin()将整数转换为二进制数
print(bin(22)) 
#print(bin(10.1)) 
print(bin(0o7))
print(bin(0xE))
#int()可以将其他进制转换为十进制
print(int(0b111))
print(int(0o77))
print(int(0xA0))
#hex()转换为十六进制
print(hex(100))
print(hex(0o777))
#oct()转换为八进制
print(oct(10))
print(oct(0xff))
